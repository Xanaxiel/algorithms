package io.codelavida.java.designpattern.facade;
/*
 * Facade Pattern:
 *    According to GOF "Provide an unified interface to a set of interfaces in a
 *    subsystem. Facade defines a higher-level abstraction that makes the sub-systems
 *    easier to use."
 *
 *    When we want to use a subset of a system or use the system in a particular way,
 *    the facade pattern comes into play. The Facade provides a collection of easier-
 *    to-understand methods.
 */